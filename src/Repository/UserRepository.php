    public function getCommentCOunt(User $user)
    {
        $conn = $this->getEntityManager()->getConnection();
        $sql = 'SELECT COUNT(c.id) as commentcount 
        FROM  comment c
        WHERE c.author_id = ' . $user->getId();
        $stmt = $conn->prepare($sql);
        $stmt->execute();
        return $stmt->fetchAll();
    }

